  EFFECT:SetPosition("effectGlitter_00", SymPos("P00_EFF_DEEDBOX_GLITTER"))
  GM("BIG_TAKARABAKO"):SetMotion(SymMot("OPEN"), LOOP.OFF)
  EFFECT:Play("effectGlitter_00")
  CH("HERO"):WaitRotate()
  CH("PARTNER"):WaitRotate()
  CH("NOKOTCHI"):WaitRotate()
  CH("BIRIJION"):WaitRotate()
  CH("BURAKKII"):WaitRotate()
  CH("EEFI"):WaitRotate()
  GM("BIG_TAKARABAKO"):WaitPlayMotion()
